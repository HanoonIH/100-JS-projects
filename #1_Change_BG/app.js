function convert(rgb) {  
    rgb = rgb.match(/^rgb\((\d+), \s*(\d+), \s*(\d+)\)$/);  
    function hexCode(i) {  
        
        // Take the last 2 characters and convert 
        // them to Hexadecimal. 
        return ("0" + parseInt(i).toString(16)).slice(-2); 
    }  
    return "#" + hexCode(rgb[1]) + hexCode(rgb[2])  
                + hexCode(rgb[3]);  
}

function changeBg() {

    var backGround = document.querySelector('body');
    let randomIndex = parseInt(Math.random() * (colorBox.length))
    backGround.style.backgroundColor = colorBox[randomIndex]

    var display = document.getElementById('display');
    var hexDisplay = document.getElementById('hexDisplay');
    var rgbDisplay = document.getElementById('rgbDisplay');

    display.textContent = colorBox[randomIndex]
    hexDisplay.textContent = convert(window.getComputedStyle(backGround).backgroundColor); 
    rgbDisplay.textContent = window.getComputedStyle(backGround).backgroundColor;

    var css = document.getElementById('css')
    var hex = document.getElementById('hex')
    var rgb = document.getElementById('rgb')

    const CSS_SHOW =  css.checked ? display.style.display = "block" : display.style.display = "none";
    const HEX_SHOW =  hex.checked ? hexDisplay.style.display = "block" : hexDisplay.style.display = "none";
    const RGB_SHOW =  rgb.checked ? rgbDisplay.style.display = "block" : rgbDisplay.style.display = "none";
}
 
